    private void bTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTotalActionPerformed
        // TODO add your handling code here:
        if (tHargaSatuan.getText().equals("") || tJumlah.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nilai Tidak boleh Kosong");
        } else {
            int n1 = Integer.parseInt(tHargaSatuan.getText());
            int n2 = Integer.parseInt(tJumlah.getText());
            int hasil = n1 * n2;
            tHargaTotal.setText(Integer.toString(hasil));
        }
    }//GEN-LAST:event_bTotalActionPerformed

    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        if (tIdPembelian.getText().equals("") || tIdSupplier.getText().equals("") || tIdBarang.getText().equals("") || tHargaSatuan.getText().equals("") || tJumlah.getText().equals("") || tHargaTotal.getText().equals("") || tTanggalPembelian.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
        } else {
            try {
               int s;
               String sql = "INSERT INTO transaksi VALUES ('"+tIdPembelian.getText()+"', '"+tIdSupplier.getText()+"', '"+tIdBarang.getText()+"', '"+tHargaSatuan.getText()+"', '"+tJumlah.getText()+"', '"+tHargaTotal.getText()+"', '"+tTanggalPembelian.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                   JOptionPane.showMessageDialog(null, "Sukses");
                   clear();
                   tampil();
               }
           } catch (Exception e){
                   System.out.println(e.toString());
           }    
        }
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        int c = JOptionPane.showConfirmDialog(null, "Ingin Mengubah Data?","Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (tIdPembelian.getText().equals("") || tIdSupplier.getText().equals("") || tIdBarang.getText().equals("") || tHargaSatuan.getText().equals("") || tJumlah.getText().equals("") || tHargaTotal.getText().equals("") || tTanggalPembelian.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
            } else {
                try {
                    int s;
                    String sql = "UPDATE transaksi SET id_supplier = '"+tIdSupplier.getText()+"', id_barang = '"+tIdBarang.getText()+"', harga_satuan = '"+tHargaSatuan.getText()+"', jumlah = '"+tJumlah.getText()+"', harga_total = '"+tHargaTotal.getText()+"', tanggal_pembelian = '"+tTanggalPembelian.getText()+"' WHERE id_pembelian = '"+tIdPembelian.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Sukses");
                        clear();
                        tampil();
                    }
                } catch (Exception e){
                        System.out.println(e.toString());
                } 
            }
        }
    }//GEN-LAST:event_bUbahActionPerformed

