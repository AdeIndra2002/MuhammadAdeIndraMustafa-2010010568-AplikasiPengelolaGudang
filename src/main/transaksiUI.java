    public void tampil(){
        Object [] baris = {"NO", "ID Pembelian", "ID Supplier", "ID Barang", "Harga Satuan", "Jumlah", "Harga Total", "Tanggal Pembelian"};
        tabMode = new DefaultTableModel(null, baris);
        tDataTransaksi.setModel(tabMode);
        try {
                String sql = "SELECT * FROM transaksi";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String ip = rs.getString("id_pembelian");
                        String is = rs.getString("id_supplier");
                        String ib= rs.getString("id_barang");
                        String hs = rs.getString("harga_satuan");
                        String jm = rs.getString("jumlah");
                        String ht = rs.getString("harga_total");
                        String tp = rs.getString("tanggal_pembelian");

                        Object [] data = {no,ip,is,ib,hs,jm,ht,tp};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
