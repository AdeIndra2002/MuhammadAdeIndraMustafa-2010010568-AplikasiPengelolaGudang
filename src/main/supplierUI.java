    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        if (tIdSupplier.getText().equals("") || tNamaSupplier.getText().equals("") || tAlamat.getText().equals("") || tNomorTelepon.getText().equals("") || tEmail.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
        } else {
            try {
               int s;
               String sql = "INSERT INTO supplier VALUES ('"+tIdSupplier.getText()+"', '"+tNamaSupplier.getText()+"', '"+tAlamat.getText()+"', '"+tNomorTelepon.getText()+"', '"+tEmail.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                   JOptionPane.showMessageDialog(null, "Sukses");
                   clear();
                   tampil();
               }
           } catch (Exception e){
                   System.out.println(e.toString());
           }    
        }
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        int c = JOptionPane.showConfirmDialog(null, "Ingin Mengubah Data?","Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (tIdSupplier.getText().equals("") || tNamaSupplier.getText().equals("") || tAlamat.getText().equals("") || tNomorTelepon.getText().equals("") || tEmail.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
            } else {
                try {
                    int s;
                    String sql = "UPDATE supplier SET nama_supplier = '"+tNamaSupplier.getText()+"', alamat = '"+tAlamat.getText()+"', nomor_telepon = '"+tNomorTelepon.getText()+"', email = '"+tEmail.getText()+"' WHERE id_supplier = '"+tIdSupplier.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Sukses");
                        clear();
                        tampil();
                    }
                } catch (Exception e){
                        System.out.println(e.toString());
                } 
            }
        }
    }//GEN-LAST:event_bUbahActionPerformed

