    public void tampil(){
        Object [] baris = {"NO", "ID Supplier", "Nama Supplier", "Alamat", "Nomor Telepon", "E-Mail"};
        tabMode = new DefaultTableModel(null, baris);
        tDataSupplier.setModel(tabMode);
        try {
                String sql = "SELECT * FROM supplier";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_supplier");
                        String ns = rs.getString("nama_supplier");
                        String al= rs.getString("alamat");
                        String nt = rs.getString("nomor_telepon");
                        String em = rs.getString("email");

                        Object [] data = {no,id,ns,al,nt,em};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
